## zh-CN

当组件在项目内使用时，有很多图片是不具备前缀地址的，需要统一添加，可以通过 `config-provider` 组件中的 `api` 参数配置统一前缀地址
也可以通过 `Image` 组件提供的 `api` 参数单独设置

